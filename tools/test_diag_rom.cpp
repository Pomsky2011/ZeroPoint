// self-test packaged as an alternate Boot ROM payload (see that file's
// header comment for what each test exercises). This tool runs it with no
// cartridge and reads back its result block so the self-test can be
// checked by CI/scripts without SDL or a display, matching CLAUDE.md's
// "use interpreter for most tests".
//
// Usage: test_diag_rom [path/to/tester.bin]
// With no argument, runs the copy of tester.asm embedded below (see
// kTesterROM) so this test is self-contained on a fresh checkout -
// examples/boot-rom/**/*.asm and *.bin are gitignored (.gitignore's broad
// "*.asm"/"*.bin" rules), so a tracked test can't depend on loading them
// from disk at runtime. Pass a path explicitly to test a freshly
// reassembled .bin while iterating on tester.asm.
