struct TestCase {
    uint32_t addr;
    const char* name;
};

static const TestCase kTests[] = {
    {0x800030, "T0 ALU add (ADC)"},
    {0x800031, "T1 ALU subtract (SBC)"},
    {0x800032, "T2 Bitwise AND/ORA/XOR"},
    {0x800033, "T3 Work RAM read/write round-trip"},
    {0x800034, "T4 PPU register bus round-trip"},
    {0x800035, "T5 APU control -> status round-trip"},
    {0x800036, "T6 Timer control round-trip"},
    {0x800037, "T7 Interrupt controller mask round-trip"},
    {0x800038, "T8 DMA controller RAM-to-RAM copy"},
};
static const uint32_t kAggregateAddr = 0x800039;
static const int kNumTests = sizeof(kTests) / sizeof(kTests[0]);

static const char* resultLabel(uint8_t v) {
    if (v == 0x01) return "PASS";
    if (v == 0x00) return "FAIL";
    if (v == 0xAA) return "NEVER RAN (crash/hang before this point)";
    return "UNEXPECTED VALUE";
}

int main(int argc, char** argv) {
    System system;
    if (argc > 1) {
        std::string bootPath = argv[1];
        if (!system.loadBootROM(bootPath)) {
            std::printf("FAIL: could not load Boot ROM: %s\n", bootPath.c_str());
            return 1;
        }
        std::printf("Loaded Boot ROM from %s\n", bootPath.c_str());
    } else {
        system.getCPU().loadBootROM(kTesterROM, kTesterROMSize);
        std::printf("Loaded embedded tester ROM (%zu bytes)\n", kTesterROMSize);
    }

    system.powerOn();

    // The whole test suite is a few hundred straight-line instructions plus
    // one DMA transfer (256 bytes at 3 cycles/byte); a couple of frames of
    // master cycles is a generous bound. Stop early if the ROM halts - which
    // it only does on FAIL now; on PASS it loops forever redrawing the "PASS"
    // text every frame (see the BRA draw_block comment in tester.asm), so the
    // wait loop below runs the full budget in that case.
    const uint64_t kMaxCycles = System::CYCLES_PER_FRAME * 4;
    uint64_t ran = 0;
    while (ran < kMaxCycles && !system.getCPU().isHalted()) {
        system.step();
        ran++;
    }

    std::printf("Ran %llu master cycles (halted=%s)\n",
                static_cast<unsigned long long>(ran),
                system.getCPU().isHalted() ? "yes" : "no");

    // The visual indicator (if drawn) lands in the *live* rolling
    // framebuffer, which only gets copied into the latched output frame
    // as the beam's H-blank passes each scanline (see
    // display-rolling-buffer-model memory / docs/display.md). Run a bit
    // further so Display finishes latching the rows the ROM drew (on PASS
    // the CPU is still looping/redrawing at this point, which is fine -
    // each pass re-derives its Y from the live beam and repaints the same
    // pixels).
    for (uint64_t i = 0; i < System::CYCLES_PER_FRAME && ran < kMaxCycles; i++, ran++) {
        system.step();
    }
    std::printf("%-42s %s\n", "Test", "Result");
    std::printf("--------------------------------------------------------\n");

    bool allPass = true;
    for (int i = 0; i < kNumTests; i++) {
        uint8_t v = system.getCPU().readByte(kTests[i].addr);
        std::printf("%-42s %s\n", kTests[i].name, resultLabel(v));
        if (v != 0x01) allPass = false;
    }

    uint8_t aggregate = system.getCPU().readByte(kAggregateAddr);
    std::printf("--------------------------------------------------------\n");
    std::printf("Aggregate byte: %s\n", resultLabel(aggregate));

    // Cross-check the visual indicator: tester.asm stashes the "PASS" text's
    // row-0 Y coordinate at $800048 whenever it draws (only when aggregate
    // == PASS). Confirm the pixels it wrote are actually visible in the
    // latched output frame, not just present in the still-live rolling
    // buffer. The glyph bitmap spans 5 rows and 15 columns (x=8..22); this
    // doesn't try to validate the shape, just that ink landed somewhere in
    // that box, matching the old 3x3-block check's level of rigor.
    bool visualOk = true;
    if (aggregate == 0x01) {
        uint8_t row0 = system.getCPU().readByte(0x800048);
        visualOk = false;
        for (int dy = 0; dy < 5 && !visualOk; dy++) {
            Color32 buffer[FB_WIDTH];
            if (system.getDisplay().getScanline(row0 + dy, buffer)) {
                for (int x = 8; x <= 22; x++) {
                    if (buffer[x] != 0) { visualOk = true; break; }
                }
            }
        }
        std::printf("Visual indicator (\"PASS\" text at y=%d): %s\n", row0,
                    visualOk ? "drawn" : "NOT FOUND in latched output frame");
    }

    // Not requiring isHalted(): FAIL halts immediately (nothing to redraw),
    // but PASS loops forever by design (see tester.asm's BRA draw_block), so
    // halted==false is the expected/correct state there, not a failure.
    bool ok = allPass && aggregate == 0x01 && visualOk;
    std::printf("\n%s\n", ok ? "PASS: all self-tests succeeded" : "FAIL: see above");
    return ok ? 0 : 1;
}
